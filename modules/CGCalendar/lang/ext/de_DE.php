<h3>Benutzerdefinierte Felder</h3>
<p>Es ist au&szlig;erdem m&ouml;glich, benutzerdefinierte Felder zu erstellen, die in der Modul-Administration &uuml;ber die Registerkarte &bdquo;Felder&ldquo; jedem Ereignis zugeordnet werden k&ouml;nnen. Entweder ein oder mehrere Felder definieren die Werte des Feldes f&uuml;r jedes Ereignis. Diese Felder k&ouml;nnen dann &uuml;ber den Registerkarte &bdquo;Ereignisse&ldquo; gef&uuml;llt werden. Die Werte dieser Felder k&ouml;nnen &uuml;ber den Syntax event.field.fieldname in den Vorlagen ausgegeben werden. Ein Tipp: geben Sie {debug} in ihre Vorlage ein, um alle Daten zu sehen, die an die Vorlage &uuml;bergeben werden.</p>

<h3>Beispiele</h3>
<h4>Ein Beispiel, wie Sie Kommentare f&uuml;r Termine/Ereignisse verwenden k&ouml;nnen:</h4>
