<p>Die Version 1.0 von Calguys Kalender ist eine Weiterentwicklung des alten Calender-Moduls. Aufgrund dessen wird es nicht zusammen mit anderen Versionen funktionieren.</p>
<h3>Was macht dieses Modul?</h3>
<p>Das Kalender-Modul ist dazu da, um einen Kalender mit Terminen/Ereignissen auf Ihrer Webseite anzuzeigen. Nach der Installation k&ouml;nnen Sie in der Administration &uuml;ber das Men&uuml; &bdquo;Inhalte > Kalender&ldquo; Ihre Termine/Ereignisse verwalten.</p>
<h3>Sicherheit</h3>
<p>Um Termine/Ereignisse hinzuf&uuml;gen, bearbeiten oder l&ouml;schen zu k&ouml;nnen, muss der Benutzer einer Gruppe mit der Berechtigung &bdquo;Modify Calendar&ldquo; angeh&ouml;ren.</p>
<h3>Wie wird es eingesetzt ?</h3>
 <ol>
  <li>F&uuml;gen Sie den {cms_module}-Tag in eine Seite ein. Achten Sie darauf, dass der Code nicht von anderen HTML-Tags (wie etwa <code>pre</code>,<code>code</code> oder &auml;hnlichem) eingeschlossen wird. Kontrollieren Sie den Quellcode. Der einzuf&uuml;gende Code sollte so aussehen:<br />
   <tt>{cms_module module=&quot;cgcalendar&quot;}</tt><br />
   <li>Gestalten Sie den Kalender via CSS nach Ihren Vorstellungen (ein Beispiel-Stylesheet wurde Ihrer CSS-Liste mit der Installation hinzugef&uuml;gt).</li>
   <li>Verbinden Sie dieses Stylesheet mit der Vorlage, welche Sie f&uuml;r die Kalender-Seite nutzen.</li>
</ol>
<h4>So verbinden Sie das mitgelieferte Stylesheet mit Ihrem Template:</h4>
<ol>
<li>Gehen Sie in der Administration in das Men&uuml; &bdquo;Layout -> Templates&ldquo;</li>
<li>Klicken Sie anschlie&szlig;end das CSS-Icon (Stylesheet mit dem Template verkn&uuml;pfen) in der Zeile des gew&uuml;nschten Templates</li>
<li>W&auml;hlen Sie dann &bdquo;Calendar CSS example&ldquo; im Listenfeldmen&uuml; aus.</li>
<li>Klicken Sie nun auf den Button &bdquo;Neues Stylesheet hinzuf&uuml;gen&ldquo;.</li>
